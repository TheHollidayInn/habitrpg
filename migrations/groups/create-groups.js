import Bluebird from 'bluebird';

import { model as Group } from '../../website/server/models/group';
import { model as User } from '../../website/server/models/user';

function createGroup(groupDetails, user) {
  let group = new Group(Group.sanitize(groupDetails));
  group.leader = user._id;

  if (group.type === 'guild') {
    // if (user.balance < 1) throw new NotAuthorized(res.t('messageInsufficientGems'));

    group.balance = 1;

    // user.balance--;
    user.guilds.push(group._id);
  } else {
    // if (group.privacy !== 'private') throw new NotAuthorized(res.t('partyMustbePrivate'));
    // if (user.party._id) throw new NotAuthorized(res.t('messageGroupAlreadyInParty'));

    user.party._id = group._id;
  }

  return group;
}

async function createGroupsFromList(groupNames, leader) {
  let promises = [];

  groupNames.forEach((groupName) => {
    let groupDetails = {
      name: groupName,
      type: 'guild',
      privacy: 'private',
    };

    let newGroup = createGroup(groupDetails, leader);

    promises.push(newGroup.save());
  });

  promises.push(leader.save());

  return await Bluebird.all(promises);
}

